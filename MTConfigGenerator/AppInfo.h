#pragma once

#include <string>
#include "stdint.h"

///////////////////////////////////////////////////////////////////////////////
//Singleton
///////////////////////////////////////////////////////////////////////////////
class AppInfo
{
public:
	~AppInfo()
	{
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////
	static AppInfo* GetAppInfoPtr()
	{
		if (m_pInstance == NULL)
			m_pInstance = new AppInfo;
		return m_pInstance;
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////////////////
	void GetVersionInfo(int& nMajor, int& nMinor, int& nBuild, int& nRevision)
	{
		nMajor		= m_nRevMajor;
		nMinor		= m_nRevMinor;
		nBuild		= m_nRevBuild;
		nRevision	= m_nRevision;
	}

protected:

private:
	static	AppInfo*	m_pInstance;
	static	int			m_nRevMajor;
	static	int			m_nRevMinor;
	static	int			m_nRevBuild;
	static	int			m_nRevision;

	AppInfo();
};